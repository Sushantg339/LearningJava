package com.company ;
public class Literals {

    public static void main(String[] args) {
        byte age = 56;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.66;
        String str = "Sushant";
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(ch);
        System.out.println(age);
        System.out.print(str);
    }
}
