public class switchCase {
    public static void main(String[] args) {
        int age = 13 ;
        switch (age){
            case 10 :{
                System.out.println("you are going to be a teenager ");
                break ;
            }
            case 11 :{
                System.out.println("you are now a teenager ");
                break ;
            }
            case 14 :{
                System.out.println("ho gyeeeeeee");
                break ;
            }
            default : {
                System.out.println("aree koi nii ho jaoge    ");
            }
        }
    }
}
